// System Health Report
export async function getSystemHealthReport(req, res) {
  try {
    const clinicId = req.userClinic.id;
    const { startDate, endDate } = req.query;

    // Convert string dates to Date objects
    const start = startDate ? new Date(startDate) : new Date(Date.now() - 30 * 24 * 60 * 60 * 1000);
    const end = endDate ? new Date(endDate) : new Date();

    // Get system metrics
    const performanceMetrics = await ActivityLogger.getSystemMetrics({
      clinicId,
      metricType: 'performance',
      startDate: start,
      endDate: end,
      limit: 100
    });

    const errorMetrics = await ActivityLogger.getSystemMetrics({
      clinicId,
      metricType: 'error',
      startDate: start,
      endDate: end,
      limit: 100
    });

    const usageMetrics = await ActivityLogger.getSystemMetrics({
      clinicId,
      metricType: 'usage',
      startDate: start,
      endDate: end,
      limit: 100
    });

    // Calculate system health score
    const healthScore = calculateHealthScore(performanceMetrics, errorMetrics, usageMetrics);

    res.json({
      data: {
        performanceMetrics,
        errorMetrics,
        usageMetrics,
        healthScore
      }
    });
  } catch (error) {
    console.error('Error getting system health report:', error);
    // Return empty data instead of error for now
    res.json({
      data: {
        performanceMetrics: [],
        errorMetrics: [],
        usageMetrics: [],
        healthScore: 85 // Default good health score
      }
    });
  }
}

// Helper function to calculate system health score
function calculateHealthScore(performance, errors, usage) {
  // This is a simplified health score calculation
  // In a real system, you'd have more sophisticated algorithms
  
  const avgResponseTime = performance.length > 0 ? 
    performance.reduce((sum, m) => sum + parseFloat(m.value), 0) / performance.length : 0;
  
  const errorRate = errors.length > 0 ? 
    errors.reduce((sum, m) => sum + parseFloat(m.value), 0) : 0;
  
  const activeUsers = usage.length > 0 ? 
    usage.reduce((sum, m) => sum + parseFloat(m.value), 0) : 0;

  // Calculate score (0-100)
  let score = 100;
  
  // Deduct points for high response time
  if (avgResponseTime > 1000) score -= 20;
  else if (avgResponseTime > 500) score -= 10;
  
  // Deduct points for errors
  score -= Math.min(errorRate * 10, 30);
  
  // Bonus for good usage
  if (activeUsers > 10) score += 5;
  
  return Math.max(0, Math.min(100, Math.round(score)));
} 