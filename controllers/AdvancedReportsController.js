// Staff Performance Report
export async function getStaffPerformanceReport(req, res) {
  try {
    const clinicId = req.userClinic.id;
    const { startDate, endDate } = req.query;

    // Convert string dates to Date objects
    const start = startDate ? new Date(startDate) : new Date(Date.now() - 30 * 24 * 60 * 60 * 1000); // 30 days ago
    const end = endDate ? new Date(endDate) : new Date();

    // Get all staff members
    const staffMembers = await db.select({
      id: userRoles.userId,
      role: userRoles.role,
      isActive: userRoles.isActive
    })
    .from(userRoles)
    .where(and(
      eq(userRoles.clinicId, clinicId),
      eq(userRoles.isActive, true)
    ));

    const staffPerformance = await Promise.all(
      staffMembers.map(async (staff) => {
        // Visits handled by this staff member
        const visitsResult = await db.select({ count: count() })
          .from(visits)
                  .where(and(
          eq(visits.clinicId, clinicId),
          eq(visits.recordedBy, staff.id),
          gte(visits.visitDate, start.toISOString()),
          lte(visits.visitDate, end.toISOString())
        ));

                // Revenue generated by this staff member
        const revenueResult = await db.select({ total: sum(payments.amount) })
          .from(payments)
          .innerJoin(invoices, eq(payments.invoiceId, invoices.id))
          .innerJoin(visits, eq(invoices.visitId, visits.id))
          .where(and(
            eq(visits.clinicId, clinicId),
            eq(visits.recordedBy, staff.id),
            gte(payments.paymentDate, start.toISOString()),
            lte(payments.paymentDate, end.toISOString())
          ));

        // Completion rate
        const completedVisitsResult = await db.select({ count: count() })
          .from(visits)
                  .where(and(
          eq(visits.clinicId, clinicId),
          eq(visits.recordedBy, staff.id),
          eq(visits.status, 'completed'),
          gte(visits.visitDate, start.toISOString()),
          lte(visits.visitDate, end.toISOString())
        ));

        const totalVisits = visitsResult[0]?.count || 0;
        const completedVisits = completedVisitsResult[0]?.count || 0;
        const revenue = parseFloat(revenueResult[0]?.total || 0);

        return {
          staffId: staff.id,
          role: staff.role,
          totalVisits,
          completedVisits,
          completionRate: totalVisits > 0 ? Math.round((completedVisits / totalVisits) * 100) : 0,
          revenue,
          averageRevenuePerVisit: totalVisits > 0 ? revenue / totalVisits : 0,
          efficiency: totalVisits > 0 ? Math.round(totalVisits / 30) : 0 // visits per day
        };
      })
    );

    res.json({ data: staffPerformance });
  } catch (error) {
    console.error('Error getting staff performance report:', error);
    // Return empty data instead of error for now
    res.json({ data: [] });
  }
}

// Patient Analytics Report
export async function getPatientAnalyticsReport(req, res) {
  try {
    const clinicId = req.userClinic.id;
    const { startDate, endDate } = req.query;

    // Convert string dates to Date objects
    const start = startDate ? new Date(startDate) : new Date(Date.now() - 30 * 24 * 60 * 60 * 1000); // 30 days ago
    const end = endDate ? new Date(endDate) : new Date();

    // Patient lifetime value
    const patientLifetimeValue = await db.select({
      patientId: patients.id,
      patientName: sql`CONCAT(${profiles.firstName}, ' ', ${profiles.lastName})`,
      totalVisits: count(visits.id),
      totalRevenue: sum(payments.amount),
      firstVisit: min(visits.visitDate),
      lastVisit: max(visits.visitDate)
    })
    .from(patients)
    .leftJoin(visits, eq(patients.id, visits.patientId))
    .leftJoin(invoices, eq(visits.id, invoices.visitId))
    .leftJoin(payments, eq(invoices.id, payments.invoiceId))
    .leftJoin(profiles, eq(patients.userId, profiles.userId))
    .where(eq(patients.clinicId, clinicId))
    .groupBy(patients.id, profiles.firstName, profiles.lastName)
    .orderBy(desc(sum(payments.amount)))
    .limit(20);

    // Patient retention analysis
    const retentionAnalysis = await db.select({
      month: sql`TO_CHAR(${visits.visitDate}, 'YYYY-MM')`,
      newPatients: count(sql`DISTINCT CASE WHEN ${visits.visitDate} = ${patients.createdAt} THEN ${patients.id} END`),
      returningPatients: count(sql`DISTINCT CASE WHEN ${visits.visitDate} > ${patients.createdAt} THEN ${patients.id} END`)
    })
    .from(visits)
    .innerJoin(patients, eq(visits.patientId, patients.id))
    .where(and(
      eq(visits.clinicId, clinicId),
      gte(visits.visitDate, startDate),
      lte(visits.visitDate, endDate)
    ))
    .groupBy(sql`TO_CHAR(${visits.visitDate}, 'YYYY-MM')`)
    .orderBy(asc(sql`TO_CHAR(${visits.visitDate}, 'YYYY-MM')`));

    // Visit frequency analysis
    const visitFrequency = await db.select({
      frequency: sql`CASE 
        WHEN visit_count = 1 THEN 'One-time'
        WHEN visit_count = 2 THEN 'Twice'
        WHEN visit_count BETWEEN 3 AND 5 THEN '3-5 visits'
        WHEN visit_count BETWEEN 6 AND 10 THEN '6-10 visits'
        ELSE '10+ visits'
      END`,
      patientCount: count(sql`DISTINCT ${patients.id}`),
      percentage: sql`ROUND(COUNT(DISTINCT ${patients.id}) * 100.0 / (SELECT COUNT(DISTINCT ${patients.id}) FROM ${patients} WHERE ${patients.clinicId} = ${clinicId}), 2)`
    })
    .from(
      db.select({
        patientId: patients.id,
        visitCount: count(visits.id)
      })
      .from(patients)
      .leftJoin(visits, eq(patients.id, visits.patientId))
      .where(eq(patients.clinicId, clinicId))
      .groupBy(patients.id)
      .as('patient_visits')
    )
    .groupBy(sql`CASE 
      WHEN visit_count = 1 THEN 'One-time'
      WHEN visit_count = 2 THEN 'Twice'
      WHEN visit_count BETWEEN 3 AND 5 THEN '3-5 visits'
      WHEN visit_count BETWEEN 6 AND 10 THEN '6-10 visits'
      ELSE '10+ visits'
    END`);

    res.json({
      data: {
        patientLifetimeValue,
        retentionAnalysis,
        visitFrequency
      }
    });
  } catch (error) {
    console.error('Error getting patient analytics report:', error);
    // Return empty data instead of error for now
    res.json({
      data: {
        patientLifetimeValue: [],
        retentionAnalysis: [],
        visitFrequency: []
      }
    });
  }
}

// Financial Deep Dive Report
export async function getFinancialDeepDiveReport(req, res) {
  try {
    const clinicId = req.userClinic.id;
    const { startDate, endDate } = req.query;

    // Convert string dates to Date objects
    const start = startDate ? new Date(startDate) : new Date(Date.now() - 30 * 24 * 60 * 60 * 1000); // 30 days ago
    const end = endDate ? new Date(endDate) : new Date();

    // Profit margin by service
    const serviceProfitability = await db.select({
      serviceName: services.name,
      totalRevenue: sum(invoiceItems.total),
      totalCost: sum(sql`${invoiceItems.total} * 0.7`), // Assuming 70% cost
      profitMargin: sql`ROUND((SUM(${invoiceItems.total}) - SUM(${invoiceItems.total} * 0.7)) / SUM(${invoiceItems.total}) * 100, 2)`,
      visitCount: count(visits.id)
    })
    .from(services)
    .innerJoin(invoiceItems, eq(services.id, invoiceItems.serviceId))
    .innerJoin(invoices, eq(invoiceItems.invoiceId, invoices.id))
    .innerJoin(visits, eq(invoices.visitId, visits.id))
    .where(and(
      eq(visits.clinicId, clinicId),
      gte(visits.visitDate, startDate),
      lte(visits.visitDate, endDate)
    ))
    .groupBy(services.id, services.name)
    .orderBy(desc(sum(invoiceItems.total)));

    // Payment method analysis
    const paymentMethodAnalysis = await db.select({
      paymentMethod: payments.paymentMethod,
      totalAmount: sum(payments.amount),
      transactionCount: count(payments.id),
      averageAmount: avg(payments.amount)
    })
    .from(payments)
    .innerJoin(invoices, eq(payments.invoiceId, invoices.id))
    .where(and(
      eq(invoices.clinicId, clinicId),
      gte(payments.paymentDate, startDate),
      lte(payments.paymentDate, endDate)
    ))
    .groupBy(payments.paymentMethod)
    .orderBy(desc(sum(payments.amount)));

    // Seasonal revenue patterns
    const seasonalPatterns = await db.select({
      month: sql`TO_CHAR(${payments.paymentDate}, 'YYYY-MM')`,
      totalRevenue: sum(payments.amount),
      averageRevenue: avg(payments.amount),
      transactionCount: count(payments.id)
    })
    .from(payments)
    .innerJoin(invoices, eq(payments.invoiceId, invoices.id))
    .where(and(
      eq(invoices.clinicId, clinicId),
      gte(payments.paymentDate, startDate),
      lte(payments.paymentDate, endDate)
    ))
    .groupBy(sql`TO_CHAR(${payments.paymentDate}, 'YYYY-MM')`)
    .orderBy(asc(sql`TO_CHAR(${payments.paymentDate}, 'YYYY-MM')`));

    // Cost per visit analysis
    const costPerVisit = await db.select({
      totalVisits: count(visits.id),
      totalRevenue: sum(payments.amount),
      estimatedCosts: sql`SUM(${payments.amount}) * 0.7`, // 70% of revenue as cost
      costPerVisit: sql`ROUND((SUM(${payments.amount}) * 0.7) / COUNT(${visits.id}), 2)`,
      profitPerVisit: sql`ROUND((SUM(${payments.amount}) * 0.3) / COUNT(${visits.id}), 2)`
    })
    .from(visits)
    .leftJoin(invoices, eq(visits.id, invoices.visitId))
    .leftJoin(payments, eq(invoices.id, payments.invoiceId))
    .where(and(
      eq(visits.clinicId, clinicId),
      gte(visits.visitDate, startDate),
      lte(visits.visitDate, endDate)
    ));

    res.json({
      data: {
        serviceProfitability,
        paymentMethodAnalysis,
        seasonalPatterns,
        costPerVisit: costPerVisit[0]
      }
    });
  } catch (error) {
    console.error('Error getting financial deep dive report:', error);
    // Return empty data instead of error for now
    res.json({
      data: {
        serviceProfitability: [],
        paymentMethods: [],
        seasonalPatterns: [],
        costAnalysis: {}
      }
    });
  }
}

